import java.util.Scanner; 
class languagebasics3 {
  public static void main(String[] args) {
    int x, y, sum;
    Scanner sc = new Scanner(System.in); 
    x = sc.nextInt(); 
    y = sc.nextInt(); 
    sum = x + y;  
    System.out.println("The Sum of " + x + " and "+ y + " is " + sum); 
  }
} 